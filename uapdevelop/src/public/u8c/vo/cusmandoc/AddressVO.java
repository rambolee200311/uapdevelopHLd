package u8c.vo.cusmandoc;

public class AddressVO {
	private String addrname;
    private String defaddrflag;
    private String pk_areacl;
    private String linkman;
    private String phone;
    public void setAddrname(String addrname) {
         this.addrname = addrname;
     }
     public String getAddrname() {
         return addrname;
     }

    public void setDefaddrflag(String defaddrflag) {
         this.defaddrflag = defaddrflag;
     }
     public String getDefaddrflag() {
         return defaddrflag;
     }

    public void setPk_areacl(String pk_areacl) {
         this.pk_areacl = pk_areacl;
     }
     public String getPk_areacl() {
         return pk_areacl;
     }

    public void setLinkman(String linkman) {
         this.linkman = linkman;
     }
     public String getLinkman() {
         return linkman;
     }

    public void setPhone(String phone) {
         this.phone = phone;
     }
     public String getPhone() {
         return phone;
     }
}
