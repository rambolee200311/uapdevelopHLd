package u8c.vo.reverseInvoice;

public class PostResult {
	private String status;
	private String adviceNote;
	private String u8cCode;
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getAdviceNote() {
		return adviceNote;
	}
	public void setAdviceNote(String adviceNote) {
		this.adviceNote = adviceNote;
	}
	public String getU8cCode() {
		return u8cCode;
	}
	public void setU8cCode(String u8cCode) {
		this.u8cCode = u8cCode;
	}
	
}
